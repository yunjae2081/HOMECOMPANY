package kr.co.haeyum.video.vo;

public class FavoriteVO {

	private int FavNo;
	private int vNo;
	private String favId;

	public int getFavNo() {
		return FavNo;
	}

	public void setFavNo(int favNo) {
		FavNo = favNo;
	}

	public int getvNo() {
		return vNo;
	}

	public void setvNo(int vNo) {
		this.vNo = vNo;
	}

	public String getFavId() {
		return favId;
	}

	public void setFavId(String favId) {
		this.favId = favId;
	}

}
