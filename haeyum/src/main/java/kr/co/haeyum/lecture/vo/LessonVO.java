package kr.co.haeyum.lecture.vo;

public class LessonVO {
	int plNo;
	int vNo;
	String plLesson;
	int plPlayTime;
	int plStayTime;
	int plX;
	int plY;
	String plColor;

	public int getPlNo() {
		return plNo;
	}

	public void setPlNo(int plNo) {
		this.plNo = plNo;
	}

	public int getvNo() {
		return vNo;
	}

	public void setvNo(int vNo) {
		this.vNo = vNo;
	}

	public String getPlLesson() {
		return plLesson;
	}

	public void setPlLesson(String plLesson) {
		this.plLesson = plLesson;
	}

	public int getPlPlayTime() {
		return plPlayTime;
	}

	public void setPlPlayTime(int plPlayTime) {
		this.plPlayTime = plPlayTime;
	}

	public int getPlStayTime() {
		return plStayTime;
	}

	public void setPlStayTime(int plStayTime) {
		this.plStayTime = plStayTime;
	}

	public int getPlX() {
		return plX;
	}

	public void setPlX(int plX) {
		this.plX = plX;
	}

	public int getPlY() {
		return plY;
	}

	public void setPlY(int plY) {
		this.plY = plY;
	}

	public String getPlColor() {
		return plColor;
	}

	public void setPlColor(String plColor) {
		this.plColor = plColor;
	}
}
