package kr.co.haeyum.lecture.vo;

public class BMarkVO {
	private int bmNo;
	private int wNo;
	private String bmPlayTime;
	private String bmTitle;
	private String bmRegDate;

	public int getBmNo() {
		return bmNo;
	}

	public void setBmNo(int bmNo) {
		this.bmNo = bmNo;
	}

	public int getwNo() {
		return wNo;
	}

	public void setwNo(int wNo) {
		this.wNo = wNo;
	}

	public String getBmPlayTime() {
		return bmPlayTime;
	}

	public void setBmPlayTime(String bmPlayTime) {
		this.bmPlayTime = bmPlayTime;
	}

	public String getBmTitle() {
		return bmTitle;
	}

	public void setBmTitle(String bmTitle) {
		this.bmTitle = bmTitle;
	}

	public String getBmRegDate() {
		return bmRegDate;
	}

	public void setBmRegDate(String bmRegDate) {
		this.bmRegDate = bmRegDate;
	}

}
